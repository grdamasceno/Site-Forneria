// One-off generator: scans the legacy cardápio image folders, copies the main
// image of each pizza into public/, and writes src/lib/products.data.ts.
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const SRC = path.join(ROOT, "cssSiteAntigo", "img", "cardapio");
const PUBLIC = path.join(ROOT, "public", "img", "cardapio");

// Folder-name keywords that indicate a sweet pizza (inside the mixed "pizzas" dir).
const DOCE = /BANANA|CHOCOLATE|ROMEU|DOCE-DE-LEITE|CHEESECAKE|KIT-?KAT|NEGRESCO|MARSHMALLOW|DUO-CHOCOLATE|CARTOLA|CREME-AVELA/i;

const SMALL = new Set(["com", "de", "e", "da", "do", "das", "dos", "a", "ao", "no", "na"]);

function titleCase(folder) {
  return folder
    .split("-")
    .map((w, i) => {
      const lower = w.toLowerCase();
      if (i > 0 && SMALL.has(lower)) return lower;
      return lower.charAt(0).toUpperCase() + lower.slice(1);
    })
    .join(" ");
}

function slugify(folder) {
  return folder.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

/** Pick the best main image in a folder (prefer the newest -V* variant). */
function pickImage(dir) {
  const files = fs
    .readdirSync(dir)
    .filter((f) => /^img-1.*\.(png|jpg|jpeg)$/i.test(f));
  if (files.length === 0) return null;
  // Prefer a -V<number> variant (newest), else plain img-1.
  files.sort((a, b) => {
    const va = (a.match(/-V(\d+)/i)?.[1] ?? "0") * 1;
    const vb = (b.match(/-V(\d+)/i)?.[1] ?? "0") * 1;
    return vb - va;
  });
  return files[0];
}

function ensureDir(d) {
  fs.mkdirSync(d, { recursive: true });
}

const products = [];

function handleLeaf(dir, folderName, category, catDir, nameOverride, slugOverride) {
  const img = pickImage(dir);
  if (!img) return;
  const slug = slugOverride ?? slugify(folderName);
  const ext = path.extname(img).toLowerCase();
  const destDir = path.join(PUBLIC, catDir);
  ensureDir(destDir);
  fs.copyFileSync(path.join(dir, img), path.join(destDir, slug + ext));
  products.push({
    name: nameOverride ?? titleCase(folderName),
    category,
    image: `/img/cardapio/${catDir}/${slug}${ext}`,
  });
}

// pizzas/ — mixed salgada + doce
const pizzasDir = path.join(SRC, "pizzas");
for (const folder of fs.readdirSync(pizzasDir)) {
  const full = path.join(pizzasDir, folder);
  if (!fs.statSync(full).isDirectory()) continue;
  const category = DOCE.test(folder) ? "pizza-doce" : "pizza-salgada";
  const catDir = DOCE.test(folder) ? "doce" : "salgada";
  handleLeaf(full, folder, category, catDir);
}

// vegana/
const veganaDir = path.join(SRC, "vegana");
for (const folder of fs.readdirSync(veganaDir)) {
  const full = path.join(veganaDir, folder);
  if (!fs.statSync(full).isDirectory()) continue;
  handleLeaf(full, folder, "vegana", "vegana");
}

// forneritos/DOCE + SALGADO
const fornDir = path.join(SRC, "forneritos");
for (const sub of fs.readdirSync(fornDir)) {
  const full = path.join(fornDir, sub);
  if (!fs.statSync(full).isDirectory()) continue;
  const name = "Fornerito " + titleCase(sub);
  handleLeaf(full, sub, "fornerito", "fornerito", name, slugify("fornerito-" + sub));
}

products.sort((a, b) => a.name.localeCompare(b.name, "pt-BR"));

const header = `// AUTO-GENERATED by scripts/gen-cardapio.mjs from the legacy cardápio images.
// Re-run the script to regenerate. Do not edit by hand.
import type { Product } from "./data";

export const products: Product[] = [
`;
const body = products
  .map((p) => `  { name: ${JSON.stringify(p.name)}, category: "${p.category}", image: "${p.image}" },`)
  .join("\n");
fs.writeFileSync(
  path.join(ROOT, "src", "lib", "products.data.ts"),
  header + body + "\n];\n",
);

// Summary
const counts = products.reduce((acc, p) => ((acc[p.category] = (acc[p.category] || 0) + 1), acc), {});
console.log(`Total: ${products.length}`);
console.log(counts);
