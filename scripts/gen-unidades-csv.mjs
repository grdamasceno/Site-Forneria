// Builds the units list from the exported CSV and downloads each storefront
// (fachada) image from the Bubble CDN into public/img/unidades.
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const CSV = path.join(
  ROOT,
  "cssSiteAntigo/img/export_All-unidades-modified--_2026-06-14_01-54-51.csv",
);
const OUTDIR = path.join(ROOT, "public", "img", "unidades");

function parseCSV(s) {
  const rows = [];
  let row = [], cur = "", q = false;
  for (let i = 0; i < s.length; i++) {
    const c = s[i];
    if (q) {
      if (c === '"') { if (s[i + 1] === '"') { cur += '"'; i++; } else q = false; }
      else cur += c;
    } else {
      if (c === '"') q = true;
      else if (c === ",") { row.push(cur); cur = ""; }
      else if (c === "\n") { row.push(cur); rows.push(row); row = []; cur = ""; }
      else if (c === "\r") { /* skip */ }
      else cur += c;
    }
  }
  if (cur.length || row.length) { row.push(cur); rows.push(row); }
  return rows;
}
function slugify(s) {
  return s.toLowerCase().normalize("NFD").replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}

const REGIOES_CSV = path.join(
  ROOT,
  "cssSiteAntigo/img/export_All-regiaos-modified_2026-06-14_02-01-29.csv",
);

const SKIP = process.env.SKIP_DOWNLOAD === "1";
const rows = parseCSV(fs.readFileSync(CSV, "utf8"));
const h = rows[0];
const col = (r, n) => (r[h.indexOf(n)] || "").trim();
const data = rows.slice(1).filter((r) => col(r, "nome") && col(r, "ativo").toLowerCase() === "yes");

// Region id -> { name, phone } map (phones are linked to the region).
const regRows = parseCSV(fs.readFileSync(REGIOES_CSV, "utf8"));
const rh = regRows[0];
const rid = rh.indexOf("unique id"), rnome = rh.indexOf("nome"), rtel = rh.indexOf("telefone");
const regions = {};
for (const r of regRows.slice(1)) {
  const id = (r[rid] || "").trim();
  if (id) regions[id] = { name: (r[rnome] || "").trim(), phone: (r[rtel] || "").trim() };
}

if (!SKIP) {
  fs.rmSync(OUTDIR, { recursive: true, force: true });
}
fs.mkdirSync(OUTDIR, { recursive: true });

const units = [];
const tasks = [];
const seen = new Set();

for (const r of data) {
  const name = col(r, "nome");
  let slug = slugify(name);
  while (seen.has(slug)) slug += "-2";
  seen.add(slug);

  const logradouro = col(r, "logradouro");
  const numero = col(r, "numero");
  const complemento = col(r, "complemento");
  const bairro = col(r, "bairro");
  const cidade = col(r, "cidade");
  const addrParts = [
    [logradouro, numero].filter(Boolean).join(", "),
    complemento,
    bairro,
  ].filter(Boolean);
  const address = addrParts.join(" - ");

  let url = col(r, "fachada");
  let ext = ".png";
  if (url) {
    const last = url.split("/").pop() || "";
    const m = last.match(/\.(png|jpe?g|webp)$/i);
    if (m) ext = "." + m[1].toLowerCase();
    if (url.startsWith("//")) url = "https:" + url;
  }
  const image = url ? `/img/unidades/${slug}${ext}` : "";

  const region = regions[col(r, "regiao")] || { name: "", phone: "" };

  units.push({
    name,
    state: col(r, "estado"),
    city: cidade,
    address,
    hours: col(r, "funcionamento"),
    phone: region.phone,
    region: region.name,
    image,
  });
  if (url) tasks.push({ url, dest: path.join(OUTDIR, `${slug}${ext}`), name });
}

async function run() {
  if (SKIP) { console.log("SKIP_DOWNLOAD=1 — mantendo imagens existentes"); return; }
  let i = 0, ok = 0, fail = 0;
  async function worker() {
    while (i < tasks.length) {
      const t = tasks[i++];
      try {
        const res = await fetch(t.url);
        if (!res.ok) throw new Error("HTTP " + res.status);
        fs.writeFileSync(t.dest, Buffer.from(await res.arrayBuffer()));
        ok++;
      } catch (e) { fail++; console.warn("FALHOU:", t.name, e.message); }
    }
  }
  await Promise.all(Array.from({ length: 8 }, worker));
  console.log(`Fachadas: ${ok} ok, ${fail} falhas`);
}
await run();

units.sort((a, b) => a.name.localeCompare(b.name, "pt-BR"));
