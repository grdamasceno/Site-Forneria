// Builds the blog/novidades posts from the exported CSV and downloads each
// post image from the Bubble CDN into public/img/blog.
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const CSV = path.join(
  ROOT,
  "cssSiteAntigo/img/export_All-blogs-modified_2026-06-14_02-18-29.csv",
);
const OUTDIR = path.join(ROOT, "public", "img", "blog");

function parseCSV(s) {
  const rows = [];
  let row = [], cur = "", q = false;
  for (let i = 0; i < s.length; i++) {
    const c = s[i];
    if (q) {
      if (c === '"') { if (s[i + 1] === '"') { cur += '"'; i++; } else q = false; }
      else cur += c;
    } else {
      if (c === '"') q = true;
      else if (c === ",") { row.push(cur); cur = ""; }
      else if (c === "\n") { row.push(cur); rows.push(row); row = []; cur = ""; }
      else if (c === "\r") { /* skip */ }
      else cur += c;
    }
  }
  if (cur.length || row.length) { row.push(cur); rows.push(row); }
  return rows;
}
function slugify(s) {
  return s.toLowerCase().normalize("NFD").replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
}
const MES = ["janeiro","fevereiro","março","abril","maio","junho","julho","agosto","setembro","outubro","novembro","dezembro"];
function formatDate(raw) {
  const d = new Date(raw);
  if (isNaN(d)) return raw;
  return `${String(d.getDate()).padStart(2, "0")} de ${MES[d.getMonth()]} de ${d.getFullYear()}`;
}

const SKIP = process.env.SKIP_DOWNLOAD === "1";
const rows = parseCSV(fs.readFileSync(CSV, "utf8"));
const h = rows[0];
const col = (r, n) => (r[h.indexOf(n)] || "").trim();
const data = rows.slice(1).filter((r) => col(r, "titulo"));

if (!SKIP) fs.rmSync(OUTDIR, { recursive: true, force: true });
fs.mkdirSync(OUTDIR, { recursive: true });

const posts = [];
const tasks = [];
const seen = new Set();

for (const r of data) {
  const title = col(r, "titulo");
  let slug = slugify(title);
  while (seen.has(slug)) slug += "-2";
  seen.add(slug);

  const text = col(r, "texto").replace(/\r/g, "").trim();
  const excerpt = text.replace(/\s+/g, " ").slice(0, 180).trim() + (text.length > 180 ? "…" : "");

  let url = col(r, "imagem");
  let ext = ".jpg";
  if (url) {
    const last = url.split("/").pop() || "";
    const m = last.match(/\.(png|jpe?g|webp)$/i);
    if (m) ext = "." + m[1].toLowerCase();
    if (url.startsWith("//")) url = "https:" + url;
  }
  const image = url ? `/img/blog/${slug}${ext}` : "";

  posts.push({
    slug,
    title,
    date: formatDate(col(r, "data")),
    sortKey: new Date(col(r, "data")).getTime() || 0,
    excerpt,
    text,
    image,
  });
  if (url) tasks.push({ url, dest: path.join(OUTDIR, `${slug}${ext}`), title });
}

async function run() {
  if (SKIP) { console.log("SKIP_DOWNLOAD=1 — mantendo imagens existentes"); return; }
  let i = 0, ok = 0, fail = 0;
  async function worker() {
    while (i < tasks.length) {
      const t = tasks[i++];
      try {
        const res = await fetch(t.url);
        if (!res.ok) throw new Error("HTTP " + res.status);
        fs.writeFileSync(t.dest, Buffer.from(await res.arrayBuffer()));
        ok++;
      } catch (e) { fail++; console.warn("FALHOU:", t.title, e.message); }
    }
  }
  await Promise.all(Array.from({ length: 6 }, worker));
  console.log(`Imagens: ${ok} ok, ${fail} falhas`);
}
await run();

// Newest first.
posts.sort((a, b) => b.sortKey - a.sortKey);
const header = `// AUTO-GENERATED by scripts/gen-blog-csv.mjs from the exported blog CSV.
// Re-run the script to regenerate. Do not edit by hand.
import type { Post } from "./data";

export const posts: Post[] = [
`;
const body = posts
  .map((p) => `  ${JSON.stringify({ slug: p.slug, title: p.title, date: p.date, excerpt: p.excerpt, text: p.text, image: p.image })},`)
  .join("\n");
fs.writeFileSync(path.join(ROOT, "src", "lib", "posts.data.ts"), header + body + "\n];\n");
console.log("Total posts:", posts.length);
